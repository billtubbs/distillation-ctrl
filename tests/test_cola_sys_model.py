def test_lv_model_controller_gains(lv_model):
    """At perturbed holdup the P-controllers produce the correct D and B.

    With M[0]=0.6 (reboiler +0.1) and M[40]=0.4 (condenser -0.1):
        B = 0.5 + 10*(0.6-0.5) = 1.5
        D = 0.5 + 10*(0.4-0.5) = -0.5  (negative — unphysical, but tests sign)
    At steady-state compositions, the holdup balances give:
        dMdt[0]  ≈ -1.0  (extra bottoms outflow of 1.0 due to B=1.5 vs 0.5)
        dMdt[40] ≈ +1.0  (reduced distillate outflow, D=-0.5 vs 0.5)
    """
    x_test = X_SS.copy()
    x_test[NT] = 0.6  # reboiler holdup perturbed +0.1
    x_test[2 * NT - 1] = 0.4  # condenser holdup perturbed -0.1

    param_vals = make_nominal_lv_param_values()
    result = lv_model.f(
        cas.DM(0.0), cas.DM(x_test), cas.DM(U_LV_NOM), *param_vals.values()
    )
    xprime = np.array(result).flatten()

    assert xprime[NT] == pytest.approx(-1.0, abs=1e-4)
    assert xprime[2 * NT - 1] == pytest.approx(1.0, abs=1e-4)


