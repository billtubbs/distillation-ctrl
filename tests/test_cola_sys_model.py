"""
Unit tests for the Skogestad-Morari Column A nonlinear distillation model
(src/dist_model_cola_cas/cola_model.py and cola_lv_model.py).

Reference values for xprime were generated by calling the original MATLAB
implementation colamod.m under GNU Octave:

    cd src/dist_model_cola_m
    octave --no-gui --eval "xprime = colamod(t, X, U); ..."

Test cases
----------
1. Uniform state (all compositions = 0.5, all holdups = M0 = 0.5),
   nominal inputs.  Only reboiler and condenser compositions move; all
   holdup derivatives are zero.

2. Linearly varying composition profile (0.1 at reboiler → 0.9 at
   condenser), all holdups at nominal, nominal inputs.  All holdup
   derivatives are zero; composition derivatives are non-trivial on
   every stage.

3. Same state as case 2 but with reflux LT perturbed to 3.0
   (nominal is 2.70629).  Tray 39 and condenser holdup derivatives
   become non-zero.
"""

