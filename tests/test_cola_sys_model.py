def make_param_values(nt=NT):
    """Return a dict of Column A nominal parameter values (numeric DM).

    Thin wrapper kept for backward compatibility with existing tests;
    new tests use make_nominal_param_values() directly.
    """
    from dist_model_cola_cas.cola_model import (
        ALPHA_DEFAULT,
        TAUL_DEFAULT,
        F0_DEFAULT,
        QF0_DEFAULT,
        L0_DEFAULT,
        L0B_DEFAULT,
        LAM_DEFAULT,
        V0_DEFAULT,
        V0T_DEFAULT,
    )

    return {
        "alpha": cas.DM(ALPHA_DEFAULT),
        "taul": cas.DM(TAUL_DEFAULT),
        "F0": cas.DM(F0_DEFAULT),
        "qF0": cas.DM(QF0_DEFAULT),
        "L0": cas.DM(L0_DEFAULT),
        "L0b": cas.DM(L0B_DEFAULT),
        "lam": cas.DM(LAM_DEFAULT),
        "V0": cas.DM(V0_DEFAULT),
        "V0t": cas.DM(V0T_DEFAULT),
        "M0": cas.DM(M0_DEFAULT * np.ones(nt)),
    }


def call_f(model, t, x, u):
    """Evaluate model.f and return xprime as a 1-D numpy array."""
    param_vals = make_param_values()
    result = model.f(
        cas.DM(t),
        cas.DM(x),
        cas.DM(u),
        *param_vals.values(),
    )
    return np.array(result).flatten()


