def test_steady_state_rootfinder(lv_model):
    """Find the Column A steady state via CasADi rootfinder and compare
    to the Octave/cola_lv.m reference (ode15s, 2000 min).

    The rootfinder solves f_lv(0, x, u_nom, params) = 0 using Newton's
    method with CasADi automatic differentiation for the Jacobian.

    Expected (from Octave simulation, norm(xprime) < 3e-9 at t=2000 min):
      xB = x_ss[0]  ≈ 0.01000  (reboiler / bottoms composition)
      xD = x_ss[40] ≈ 0.99000  (condenser / distillate composition)
      M[i] = 0.5 for all i     (all holdups at nominal)
    """
    param_vals = make_nominal_lv_param_values()

    # Build g(x) = f_lv(0, x, u_nom, params_nom)
    x_sym = cas.SX.sym("x", lv_model.n)
    g_expr = lv_model.f(
        cas.DM(0.0), x_sym, cas.DM(U_LV_NOM), *param_vals.values()
    )
    g_func = cas.Function("g", [x_sym], [g_expr], ["x"], ["g"])

    rf = cas.rootfinder("rf", "newton", g_func)
    x0 = 0.5 * np.ones(lv_model.n)
    x_ss = np.array(rf(x0)).flatten()

    # Residual at solution should be near machine precision
    residual = np.array(g_func(x_ss)).flatten()
    assert np.linalg.norm(residual) < 1e-8

    # Key compositions match Octave reference to 4 decimal places
    assert x_ss[0] == pytest.approx(0.01000, abs=1e-4)  # xB (reboiler)
    assert x_ss[40] == pytest.approx(0.99000, abs=1e-4)  # xD (condenser)

    # All holdups at nominal value
    assert np.allclose(x_ss[NT:], 0.5, atol=1e-6)

    # Full composition profile matches Octave reference to 4 decimal places
    assert np.allclose(x_ss[:NT], X_SS_COMP, atol=1e-4)


